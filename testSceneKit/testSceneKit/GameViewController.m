{
    [super viewDidLoad];

    // create a new scene
    SCNScene *scene = [SCNScene scene];
    
//    SCNNode *rootNode = [SCNScene sceneNamed:@"art.scnassets/Missile/missile.dae"].rootNode;
    SCNNode *rootNode = [SCNScene sceneNamed:@"art.scnassets/Rose/rose_flower.dae"].rootNode;
    
    [scene.rootNode addChildNode:rootNode];
    
    NSArray *array = rootNode.childNodes;
    NSLog(@"%@",array);
    
